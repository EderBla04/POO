package Uni4Ejemplos;

public class Tipoboleto {
	
	private String nombre;
	
	private double precio;

	public Tipoboleto(String nombre, double precio) {
		super();
		this.nombre = nombre;
		this.precio = precio;
	}

	public String getNombre() {
		return nombre;
	}

	public double getPrecio() {
		return precio;
	}
	
	



}
